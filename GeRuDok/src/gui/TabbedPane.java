package gui;

import java.util.ArrayList;

import javax.swing.JTabbedPane;

import view.ProjectView;

public class TabbedPane extends JTabbedPane
{
	private ArrayList<ProjectView> projectViews = new ArrayList<ProjectView>();

	public TabbedPane()
	{
		super();
	}
	
	public ArrayList<ProjectView> getProjectViews() {
		return projectViews;
	}

	public void setProjectViews(ArrayList<ProjectView> projectView) {
		this.projectViews = projectView;
	}
}
